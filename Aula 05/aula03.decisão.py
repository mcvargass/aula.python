n1 = float(input("Digite sua primeira nota: "))
n2 = float(input("Digite sua primeira nota: "))

média = (n1 + n2) / 2
print("Sua média é: "(média))

if média >= 7:
    print("Você atingiu a média:")

elif média == 7:
    print("Você atingiu a média!" )

else:
     print("Você não atingiu a média! :( ")













