# Tipo Primitivo booleano
b = input ("Digite alguma coisa aqui: ")

print("Existe espaço?" , b.isspace())
print("Existe caracteres numericos?", b.isnumeric())
print("Existe caracteres alfabeticos?", b.isalpha())
print("Existe caracteres alfabeticos?", b.isalnum())
print("Espaço entre textos?", " " in b) #Verifica o que possui dentro do " ".
